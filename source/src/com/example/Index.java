package com.example;

public class Index {

    private int length;
    private int location;

    public Index(int length, int location) {
        this.length = length;
        this.location = location;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

    public int getLocation() {
        return location;
    }

    public void setLocation(int location) {
        this.location = location;
    }
}
